    private void btnBuscarXDNIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarXDNIActionPerformed

        if (txtDni.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese el DNI del paciente.");
            return;
        }

        try {
            ResultSet rsPaciente = objPaciente.buscarPacientePorDNIPago(txtDni.getText());

            if (rsPaciente.next()) {

                txtPaciente.setText(rsPaciente.getString("nombres") + " "
                        + rsPaciente.getString("apellidoPaterno") + " "
                        + rsPaciente.getString("apellidoMaterno"));
                txtTelefono.setText(rsPaciente.getString("telefono"));
            } else {
                JOptionPane.showMessageDialog(this, "No se encontró un paciente con el DNI proporcionado.");
                limpiarCamposPaciente();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al buscar el paciente: " + e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarXDNIActionPerformed

