    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            // Validar campos requeridos
            if (txtDni.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor, ingrese el DNI del paciente.");
                return;
            }

            if (tblCita.getRowCount() == 0 && tblServicios.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Debe añadir al menos una cita o un servicio para registrar el pago.");
                return;
            }

            if (cboMetodoPago.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar un método de pago.");
                return;
            }

            if (txtSubtotal.getText().isEmpty() || txtIgv.getText().isEmpty() || txtMonto_Total.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Los totales no pueden estar vacíos.");
                return;
            }

            double subtotal = Double.parseDouble(txtSubtotal.getText());
            double igv = Double.parseDouble(txtIgv.getText());
            double montoTotal = Double.parseDouble(txtMonto_Total.getText());

            if (subtotal <= 0 || igv < 0 || montoTotal <= 0) {
                JOptionPane.showMessageDialog(this, "Los valores de subtotal, IGV y monto total deben ser mayores que cero.");
                return;
            }

            int idPaciente = objPaciente.obtenerCodigoPacienteXDNI(txtDni.getText());
            if (idPaciente == 0) {
                JOptionPane.showMessageDialog(this, "No se encontró un paciente con el DNI ingresado.");
                return;
            }

            int idPago = objPago.generarCodigoPago();
            int idMetodoPago = cboMetodoPago.getSelectedIndex() + 1;
            boolean estado = chkEstado.isSelected();

            objPago.registrarPago(
                    idPago, estado, idPaciente, idMetodoPago,
                    tblCita, tblServicios
            );

            JOptionPane.showMessageDialog(this, "Pago registrado correctamente.");
            limpiarControles();

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Error en los valores numéricos. Verifique los totales.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al guardar el pago: " + e.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

