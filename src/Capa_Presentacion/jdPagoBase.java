    private void agregarCita(int idCita, double precio) {
        DefaultTableModel modeloCitas = (DefaultTableModel) tblCita.getModel();

        for (int i = 0; i < modeloCitas.getRowCount(); i++) {
            int idExistente = Integer.parseInt(modeloCitas.getValueAt(i, 0).toString());
            if (idExistente == idCita) {
                JOptionPane.showMessageDialog(this, "La cita con ID " + idCita + " ya ha sido añadida.");
                return;
            }
        }

        txtId_Cita.setText(String.valueOf(idCita));
        txtPrecioCita.setText(String.format("%.2f", precio));

        String fecha = objAniadirCita.getFechaCitaSeleccionada();
        String motivo = objAniadirCita.getMotivoCitaSeleccionada();

        modeloCitas.addRow(new Object[]{
            idCita,
            fecha,
            motivo,
            precio
        });

        actualizarTotales();
    }

    private void agregarServicio(int idServicio, String nombreServicio, double precioServicio) {
        DefaultTableModel modeloServicios = (DefaultTableModel) tblServicios.getModel();

        for (int i = 0; i < modeloServicios.getRowCount(); i++) {
            int idExistente = Integer.parseInt(modeloServicios.getValueAt(i, 0).toString());
            if (idExistente == idServicio) {
                JOptionPane.showMessageDialog(this, "El servicio con ID " + idServicio + " ya ha sido añadido.");
                return;
            }
        }
        modeloServicios.addRow(new Object[]{
            idServicio,
            nombreServicio,
            precioServicio
        });
        actualizarTotales();
    }

    private void actualizarTotales() {
        double subtotal = 0.0;

        DefaultTableModel modeloCitas = (DefaultTableModel) tblCita.getModel();
        for (int i = 0; i < modeloCitas.getRowCount(); i++) {
            subtotal += Double.parseDouble(modeloCitas.getValueAt(i, 3).toString());
        }
        DefaultTableModel modeloServicios = (DefaultTableModel) tblServicios.getModel();
        for (int i = 0; i < modeloServicios.getRowCount(); i++) {
            subtotal += Double.parseDouble(modeloServicios.getValueAt(i, 2).toString());
        }
        double igv = subtotal * 0.18;
        double montoTotal = subtotal + igv;

        txtSubtotal.setText(String.format("%.2f", subtotal));
        txtIgv.setText(String.format("%.2f", igv));
        txtMonto_Total.setText(String.format("%.2f", montoTotal));
    }

    private void listarMetodosPago() {
        ResultSet rs = null;
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        cboMetodoPago.setModel(modelo);
        try {
            rs = objMetodoPago.listarMetodoPago();
            while (rs.next()) {
                modelo.addElement(rs.getString("nombre"));
            }
        } catch (Exception e) {
        }
    }

