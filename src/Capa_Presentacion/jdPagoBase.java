    private void btnQuitarCitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarCitaActionPerformed

        int filaSeleccionada = tblCita.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, selecciona una cita para quitar.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                "¿Estás seguro de que deseas quitar la cita seleccionada?",
                "Confirmar",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            DefaultTableModel modeloCitas = (DefaultTableModel) tblCita.getModel();
            modeloCitas.removeRow(filaSeleccionada);

            actualizarTotales();
            txtId_Cita.setText("");
            txtPrecioCita.setText("");
            JOptionPane.showMessageDialog(this, "La cita ha sido quitada exitosamente.");
        }
    }//GEN-LAST:event_btnQuitarCitaActionPerformed

