    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        ResultSet rs = null;
        try {
            if (txtId.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Debe ingresar en codigo del  Metodo de pago");
            } else {
                rs = objMetodo.buscarMetodoPago(Integer.parseInt(txtId.getText()));
                if (rs.next()) {
                    txtNombre.setText(rs.getString("nombre"));
                    rs.close();
                } else {
                    JOptionPane.showMessageDialog(this, "Id de servicio no existe");
                    limpiarControles();
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al buscar Metodo de pago");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
        try {
            if (btnNuevo.getText().equals("Nuevo")) {
                btnNuevo.setText("Guardar");
                limpiarControles();
                txtId.setEditable(false);
                txtId.setText(objMetodo.generarCodigo().toString());
                JOptionPane.showMessageDialog(this, "Complete campos para el registro");
               
                txtNombre.requestFocus();

            } else {
                btnNuevo.setText("Nuevo");
                objMetodo.registrarMetodo(Integer.parseInt(txtId.getText()), txtNombre.getText());
                JOptionPane.showMessageDialog(this, "Metodo de pago registrado");
                limpiarControles();
                listarMetodos();
                txtId.setEditable(true);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al registrar nuevo Metodo de pago--->" + e.getMessage());
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

