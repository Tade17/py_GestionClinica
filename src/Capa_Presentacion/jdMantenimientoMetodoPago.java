    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        try {
            int respuesta = JOptionPane.showOptionDialog(null, "¿Estas seguro que quieres eliminar este Metodo de pago --->" + txtNombre.getText() + " ?", "Pregunta",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, new Object[]{"Si", "No"}, "Si");
            if (respuesta == 0) {
                objMetodo.eliminarMetodo_Pago(Integer.parseInt(txtId.getText()));
                JOptionPane.showMessageDialog(this, "Metodo de pago eliminado");
                limpiarControles();
                listarMetodos();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar Metodo de pago" + e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

