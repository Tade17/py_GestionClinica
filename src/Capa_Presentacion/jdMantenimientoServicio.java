    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        ResultSet rsServicio = null;
        try {
            if (txtId.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Debe ingresar en codigo del  servicio");
            } else {
                rsServicio = objServicio.buscarServicio(Integer.parseInt(txtId.getText()));
                if (rsServicio.next()) {
                    txtNombre.setText(rsServicio.getString("nombre"));
                    txtDescripcion.setText(rsServicio.getString("descripcion"));
                    txtPrecio.setText(rsServicio.getString("precio"));
                    chkEstado.setSelected(rsServicio.getBoolean("estado"));
                    rsServicio.close();
                } else {
                    JOptionPane.showMessageDialog(this, "Id de servicio no existe");
                    limpiarControles();
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al buscar servicio");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
        try {
            if (btnNuevo.getText().equals("Nuevo")) {
                btnNuevo.setText("Guardar");
                limpiarControles();
                txtId.setEditable(false);
                chkEstado.setSelected(true);
                txtId.setText(objServicio.generarCodigoServicio().toString());
                JOptionPane.showMessageDialog(this, "Complete campos para el registro");
                txtNombre.requestFocus();

            } else {
                if (validarCampos()) {
                    btnNuevo.setText("Nuevo");
                    objServicio.registrarServicio(Integer.parseInt(txtId.getText()), txtNombre.getText(), txtDescripcion.getText(), Double.valueOf(txtPrecio.getText()),
                            chkEstado.isSelected());
                    txtId.setEditable(true);
                    limpiarControles();
                    listarServicios();
                    JOptionPane.showMessageDialog(this, "Servicio registrado");
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al registrar nuevo servicio--->" + e.getMessage());
        }
    }//GEN-LAST:event_btnNuevoActionPerformed
    private boolean validarCampos() {
        if (txtId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un ID para el servicio.");
            return false;
        }
        if (txtNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un nombre para el servicio");
            return false;
        }
        if (txtNombre.getText().length() < 3 || txtNombre.getText().length() > 100) {
            JOptionPane.showMessageDialog(this, "El nombre debe tener entre 3 y 100 caracteres.");
            return false;
        }
        if (txtDescripcion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar una descripción para el servicio.");
            return false;
        }
        if (txtPrecio.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar el precio del servicio");
            return false;
        }
        if (Double.parseDouble(txtPrecio.getText()) <= 0) {
            JOptionPane.showMessageDialog(this, "Debe ingresar el precio correcto");
            return false;
        }

        return true;
    }
