    private void listarServicios() {
        ResultSet rsServicio = null;
        Vector registro;
        String vigencia = "";
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("Descripcion");
        modelo.addColumn("Precio");
        modelo.addColumn("Estado");
        try {
            rsServicio = objServicio.listarServicios();
            while (rsServicio.next()) {
                if (rsServicio.getString("estado").equals("t")) {
                    vigencia = "Disponible";
                } else {
                    vigencia = "No Disponible";
                }
                registro = new Vector();
                registro.add(0, rsServicio.getInt("idServicio"));
                registro.add(1, rsServicio.getString("nombre"));
                registro.add(2, rsServicio.getString("descripcion"));
                registro.add(3, rsServicio.getDouble("precio"));
                registro.add(4, vigencia);
                modelo.addRow(registro);
            }

            tblServicio.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al listar las servicios" + e.getMessage());
        }
    }


