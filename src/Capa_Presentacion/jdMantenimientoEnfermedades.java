    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        ResultSet rs = null;
        try {
            if (txtId.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Debe ingresar en codigo de la Enfermedad");
            } else {
                rs = objEn.buscarEnfermedad(Integer.parseInt(txtId.getText()));
                if (rs.next()) {
                    txtNombre.setText(rs.getString("nombre"));
                    txtDescripcion.setText(rs.getString("descripcion"));
                    rs.close();
                } else {
                    JOptionPane.showMessageDialog(this, "Id de Enfermedad no existe");
                    limpiarControles();
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al buscar Enfermedad");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void limpiarControles() {
        txtId.setText("");
        txtNombre.setText("");
        txtDescripcion.setText("");
    }

    private void listarEnfermedades() throws Exception {
        ResultSet rs = null;
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("Id_enfermedad");
            modelo.addColumn("Nombre");
            modelo.addColumn("Descripcion");
            rs = objEn.listarEnfermedades();
            Object datos[][] = new Object[1][3];
            while (rs.next()) {
                datos[0][0] = rs.getInt("idEnfermedad");
                datos[0][1] = rs.getString("nombre");
                datos[0][2] = rs.getString("descripcion");
                modelo.addRow(datos[0]);
            }
            tblEnfermedad.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
