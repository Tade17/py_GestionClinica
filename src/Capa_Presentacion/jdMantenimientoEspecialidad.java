    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed

        try {
            if (btnNuevo.getText().equals("Nuevo")) {
                btnNuevo.setText("Guardar");
                limpiarControles();
                txtId.setEditable(false);
                txtId.setText(objEspecialidad.generarCodigo().toString());
                JOptionPane.showMessageDialog(this, "Complete campos para el registro");
                txtNombre.requestFocus();

            } else {
                if (validarCampos()) {
                    btnNuevo.setText("Nuevo");
                    objEspecialidad.registrarEspecialidad(Integer.parseInt(txtId.getText()), txtNombre.getText(), txtDescripcion.getText(), this.chkVigencia.isSelected());
                    limpiarControles();
                    listarEspecialidades();
                    txtId.setEditable(true);
                    JOptionPane.showMessageDialog(this, "Especialidad Registrada");
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al registrar nueva especialidad--->" + e.getMessage());
        }
    }//GEN-LAST:event_btnNuevoActionPerformed
    private boolean validarCampos() {
        if (txtId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un ID para la especialidad.");
            return false;
        }
        if (txtNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un nombre para la especialidad.");
            return false;
        }
        if (txtNombre.getText().length() < 3 || txtNombre.getText().length() > 100) {
            JOptionPane.showMessageDialog(this, "El nombre debe tener entre 3 y 100 caracteres.");
            return false;
        }
        if (txtDescripcion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar una descripción para la especialidad.");
            return false;
        }

        return true;
    }
