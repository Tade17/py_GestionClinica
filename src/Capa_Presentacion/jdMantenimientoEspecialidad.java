    private void txtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyTyped

        // TODO add your handling code here:
        int key = evt.getKeyChar();

        boolean mayusculas = key >= 65 && key <= 90;
        boolean minusculas = key >= 97 && key <= 122;
        boolean espacio = key == 32;

        if (!(minusculas || mayusculas || espacio)) {
            evt.consume();
        }
    }//GEN-LAST:event_txtNombreKeyTyped

    private void txtDescripcionKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDescripcionKeyTyped

        int key = evt.getKeyChar();

        boolean letra = Character.isLetter(key);
        boolean espacio = key == 32;
        boolean numero = Character.isDigit(key);

        if (!(letra || espacio || numero)) {
            evt.consume();
        }

    }//GEN-LAST:event_txtDescripcionKeyTyped

    private void listarEspecialidades() {
        ResultSet rsEspecialidad = null;
        Vector registro;
        String vigencia = "";
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("Descripcion");
        modelo.addColumn("Vigencia");
        try {
            rsEspecialidad = objEspecialidad.listarEspecialidades();
            while (rsEspecialidad.next()) {
                if (rsEspecialidad.getString("estado").equals("t")) {
                    vigencia = "Vigente";
                } else {
                    vigencia = "No vigente";
                }
                registro = new Vector();
                registro.add(0, rsEspecialidad.getInt("idEspecialidad"));
                registro.add(1, rsEspecialidad.getString("nombre"));
                registro.add(2, rsEspecialidad.getString("descripcion"));
                registro.add(3, vigencia);
                modelo.addRow(registro);
            }

            tblEspecialidad.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al listar las especialidades" + e.getMessage());
        }
    }

