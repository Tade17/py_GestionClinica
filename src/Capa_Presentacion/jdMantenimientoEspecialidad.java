    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        ResultSet rsEspecialidad = null;
        try {
            if (txtId.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Debe ingresar en codigo de la especialidad");
            } else {
                rsEspecialidad = objEspecialidad.buscarEspecialidad(Integer.parseInt(txtId.getText()));
                if (rsEspecialidad.next()) {
                    txtNombre.setText(rsEspecialidad.getString("nombre"));
                    txtDescripcion.setText(rsEspecialidad.getString("descripcion"));
                    chkVigencia.setSelected(rsEspecialidad.getBoolean("estado"));
                    rsEspecialidad.close();
                } else {
                    JOptionPane.showMessageDialog(this, "Id de categoria no existe");
                    limpiarControles();
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al buscar especialidad");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

